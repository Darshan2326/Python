public class EqualityOperators {
    
    public static void equalityOperations(int a, int b) {
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
    }
    
    public static void main(String[] args) {
        equalityOperations(10, 20);
    }
}
