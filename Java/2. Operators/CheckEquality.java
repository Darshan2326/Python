public class CheckEquality {
    
    public static void checkEquality(int a, int b) {
        if (a == b) {
            System.out.println("The numbers are equal.");
        } else {
            System.out.println("The numbers are not equal.");
        }
    }
    
    public static void main(String[] args) {
        checkEquality(10, 20);
    }
}
